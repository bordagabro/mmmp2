    global $selLang;
    global $sscRespHu;
    global $sscRespEn;

    $p = 0;
    foreach($lang as $i){
        if(array_search($i, $availLangs)){
            if($p == 0){
                $p = 1;
                $selLang = $i;
            }
        }
    }

    if($p == 0){
        echo "E";
    }
    else{
        if($selLang == 'HU'){
            return $sscRespHu[$code];
        }
        else if($selLang == 'EN'){
            return $sscRespEn[$code];
        }
    }
}




